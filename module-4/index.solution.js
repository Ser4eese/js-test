/**
 * Модуль 4: Циклы (for, while)
 *
 * Решение: Функция sumArray суммирует элементы массива используя цикл for
 *
 * @param {number[]} arr - Массив чисел для суммирования
 * @returns {number} Сумма всех элементов массива
 */

function sumArray(arr) {
  // Создаем переменную для хранения суммы, инициализируем нулем
  let sum = 0;

  // Используем цикл for для перебора каждого элемента массива
  // i начинается с 0 (первый индекс массива)
  // цикл продолжается пока i меньше длины массива
  // после каждой итерации i увеличивается на 1
  for (let i = 0; i < arr.length; i++) {
    // Прибавляем текущий элемент массива к общей сумме
    sum += arr[i];
  }

  // Возвращаем итоговую сумму
  return sum;
}

module.exports = sumArray;
